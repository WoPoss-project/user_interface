                <h2>Survey about words of fear</h2>
<img src="images/news/word-cloud.png" alt="word cloud multilingual words of fear" class="cloud">
                <p>We are currently working on an exposition concerning the words of fear, especially
                    those that have taken a positive value throughout time. We have individuated for now three types of
                    words:</p>
                <ol>
                    <li>As in French <em>formidable</em>: etymologically related to the feeling of fear (Latin
                        <em>formidabilis</em>
                        ⇒ “inspiring fear, frightening, terrifying”), it is now mainly used with the meaning “great,
                        fantastic”;
                    </li>
                    <li>As in English <em>terrific</em>: also etymologically related to fear (Latin <em>terreo</em> ⇒ “to terrorize”) it
                        can
                        now be used both with a positive (“amazing”) and a negative (“terrible”) meaning;
                    </li>

                    <li>As in French <em>terriblement</em>, German <em>schrecklich</em>, English <em>awfully</em>, adverbs etymologically related
                        to
                        fear and that can nowadays intensify an adjective in both a positive and a negative way.
                    </li>
                </ol>

                <p>We would like to enlarge the list as much as possible with words matching these three categories
                and coming from as many languages as possible. Do you happen to have any ideas and feel like you
                would like to contribute to our list? If so, please feel free to contact us: <a href="mailto:woposs@unil.ch">woposs@unil.ch</a>
                </p>
